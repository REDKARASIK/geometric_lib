import math
'''Import library with math constants and functions'''

def area(r):
    '''function that get raduis of circle and return math.pi (3,14) * r^2 = area'''
    return math.pi * r * r


def perimeter(r):
    '''function that get raduis of circle and return 2 * math.pi (3,14) * r = perimeter'''
    return 2 * math.pi * r

